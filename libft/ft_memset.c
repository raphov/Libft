#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*ch;

	i = 0;
	ch = (char *)s;
	while (i < n)
	{
		ch[i] = c;
		i++;
	}
	return (s);
}
